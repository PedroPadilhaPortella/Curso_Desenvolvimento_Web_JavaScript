/*           First class Function
 *  A programming language is said to have First-class 
 *    funcitons when functions in that language
 *    are treated like any other variable.
*/

let x = 10
const sum = (a, b) => a + b
const sub = (a, b) => a - b
const multi = (a, b) => a * b
const div = (a, b) => a / b


console.log(sum(x, 89))
console.log(sub(x, 89))
console.log(multi(x, 89))
console.log(div(x, 89))